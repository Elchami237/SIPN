            };

            const observer = new IntersectionObserver(function(entries) {
                entries.forEach(entry => {
                    if (entry.isIntersecting) {
                        entry.target.classList.add('animate-fade-in-up');
                    }
                });
            }, observerOptions);

            // Observer tous les éléments avec la classe animate-on-scroll
            document.querySelectorAll('.animate-on-scroll').forEach(el => {
                observer.observe(el);
            });

            // Animation des statistiques au scroll
            const statsObserver = new IntersectionObserver(function(entries) {
                entries.forEach(entry => {
                    if (entry.isIntersecting) {
                        const statNumbers = entry.target.querySelectorAll('[data-stat]');
                        statNumbers.forEach(stat => {
                            const finalValue = parseInt(stat.getAttribute('data-stat'));
                            animateNumber(stat, 0, finalValue, 2000);
                        });
                    }
                });
            }, observerOptions);

            // Observer les sections de statistiques
            document.querySelectorAll('.stats-section').forEach(el => {
                statsObserver.observe(el);
            });

            // Fonction d'animation des nombres
            function animateNumber(element, start, end, duration) {
                const startTime = performance.now();
                
                function updateNumber(currentTime) {
                    const elapsed = currentTime - startTime;
                    const progress = Math.min(elapsed / duration, 1);
                    
                    const current = Math.floor(start + (end - start) * progress);
                    element.textContent = current + (end >= 100 ? '+' : '');
                    
